# math.pl

$x = 3.14159; 
$y = sin($x); 
print("y = $y\n");

$z = cos($y); 
print("z = $z\n");
# atan2 function calculates and returns 
# the argument of one value divided by another, 
# in the range -pi to pi 
$u = atan2(1.0,4.0); 
print("u = $u\n");

$v = sqrt(6.5); 
print("v = $v\n");

$w = exp($v); 
print("w = $w\n");

$r = log(4.0); # log base e 
print("r = $r\n");

$s = abs(-3.5); 
print("s = $s\n");
